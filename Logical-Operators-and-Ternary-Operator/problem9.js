// switch case
// EX-1

let weather_app1= "rainy";

switch(weather_app1){

  case "sunny":
    console.log("It is a beautiful day");
    break;
  case "cloudy":
    console.log("It is a bit cloudy, but still pleasant");
    break;
  case "rainy":
    console.log("it is rainning today");
    break;
  default: 
    console.log("sorry, we do not have current weather information.")
}

